import conf from "../conf/conf";
import { Client, ID, Databases, Storage, Query, TablesDB } from "appwrite";

export class Service{
    client = new Client();
    databases;
    bucket;
    constructor() {
        this.client
            .setEndpoint(conf.appwriteUrl)
            .setProject(conf.appwriteProjectId);
        this.databases = new TablesDB(this.client);
        this.bucket = new Storage(this.client);
    }

    async createPost({title, slug,content, featuredImage, status, userId})
    {
        try {
            return await this.databases.createRow({
                databaseId :conf.appwriteDatabaseId,
                tableId: conf.appwriteCollectionId,
                rowId: ID.unique(),
                data: 
                {
                    title,
                    content,
                    featuredImage,
                    status,
                    userId,
                }
        })
        } catch (error) {
            console.log("Appwrite service :: CreatePost:: error",error);
        }
    }

    async updatePost(slug,{title,content, featuredImage, status})
    {
        try {
            return await this.databases.updateRow({
                databaseId: conf.appwriteDatabaseId,
                tableId: conf.appwriteCollectionId,
                rowId: slug,
                data: {
                    title,
                    content,
                    featuredImage,
                    status
                }
                
            })
        } catch (error) {
            console.log("Appwrite service :: UpdatePost:: error",error);
            
        }
    }

    async deletePost(slug)
    {
        try {
            await this.databases.deleteRow({
                databaseId: conf.appwriteDatabaseId,
                tableId: conf.appwriteCollectionId,
                rowId: slug,
                queries: [
                Query.equal("id", slug) // delete the row where id = slug
            ]
            })
        } catch (error) {
            console.log("Appwrite service :: deletePost:: error",error);
            return false;
        }
    }

    async getPost(slug)
    {
        try {
            return await this.databases.getRow({
                databaseId: conf.appwriteDatabaseId,
                tableId: conf.appwriteCollectionId,
                rowId: slug,

            })
        } catch (error) {
            console.log("Appwrite service :: getPost:: error",error);
            return false;
        }
    }

    async getPosts(queries = [Query.equal("status","active")])
    {
        try {
            const response =  await this.databases.listRows({
                databaseId: conf.appwriteDatabaseId,
                tableId: conf.appwriteCollectionId,
                queries: queries,

            });
            return response;
        } catch (error) {
            console.log("Appwrite service :: get All Posts :: error",error); 
            return false;
        }
    }

    // file upload service
    async uploadFile(file)
    {
        try {
            return await this.bucket.createFile({
                bucketId:conf.appwriteBucketId,
                fileId: ID.unique(),
                file: file
            })
        } catch (error) {
            console.log("Appwrite service :: Upload File :: error ",error);
            return false;
        }
    }

    async deleteFile(fileId)
    {
        try {
            await this.bucket.deleteFile({
                bucketId: conf.appwriteBucketId,
                fileId: fileId
            })
            return true;
        } catch (error) {
            console.log("Appwrite service :: deleteFile:: error ",error);
            return false;
        }
    }

     getFilePreview(fileId)
    {
        try {
            return this.bucket.getFileView({
                bucketId: conf.appwriteBucketId,
                fileId: fileId
            })
        } catch (error) {
            console.log("Appwrite service :: getFilePreview :: error",error);
            return false;
        }
    }
}

const service = new Service()

export default service

